package com.entity;

public class OrdinaryUsers {
	int o_userid;
	String  o_user;
	String o_user_password;
	public int getO_userid() {
		return o_userid;
	}
	public void setO_userid(int o_userid) {
		this.o_userid = o_userid;
	}
	public String getO_user() {
		return o_user;
	}
	public void setO_user(String o_user) {
		this.o_user = o_user;
	}
	public String getO_user_password() {
		return o_user_password;
	}
	public void setO_user_password(String o_user_password) {
		this.o_user_password = o_user_password;
	}
	
}
